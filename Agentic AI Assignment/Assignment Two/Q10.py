#Problem 10: Calculate Average Marks

# Step 1: Initialize the list of marks
marks = [85, 70, 60, 80, 65, 75]

# Step 2: Calculate the sum of all marks
total_sum = sum(marks)

# Step 3: Find the total number of marks
count = len(marks)

# Step 4: Calculate the average
# Formula: Sum of all observations / Total number of observations
average_marks = total_sum / count

# Step 5: Display the result
print(f"Average Marks: {average_marks}")