#Problem 8: Convert List to Tuple

# Step 1: Initialize the list of numbers
input_list = [10, 20, 30]

# Step 2: Convert the list to a tuple using the tuple() function
# Tuples are immutable, meaning they cannot be modified after creation
converted_tuple = tuple(input_list)

# Step 3: Display the result
print(f"Tuple: {converted_tuple}")